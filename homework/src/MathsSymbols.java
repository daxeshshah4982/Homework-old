public class MathsSymbols {
    public static void main(String[] arg) {
        int a = 30;
        int b = 20;
        int c = 10;
        int d = 0;

        System.out.println(a+b+c+d);
        System.out.println(d-b-a-d);
        System.out.println(a*a*c*d);
        System.out.println(a*a*c/c);
        System.out.println(c/b-c+d);
        System.out.println(a+b-c*d);
        System.out.println(d-b/c-d);
        System.out.println(a*b*c-d);
        System.out.println(a-a+c-d);
        System.out.println(c+b/a*d);
        System.out.println(a*d/c-d);
        System.out.println(a+b*c-d);

    }
}
